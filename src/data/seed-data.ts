const lesson = (
  courseSlug: string,
  slug: string,
  title: string,
  content: string,
  flags: { assessment?: boolean; vr?: boolean } = {},
) => ({
  slug: `${courseSlug}-${slug}`,
  title,
  type: flags.vr ? ("vr-practice" as const) : flags.assessment ? ("assessment" as const) : ("text" as const),
  content,
  videoUrl: "https://video.example.com/vowlms/placeholder",
  hasAssessment: Boolean(flags.assessment),
  hasVRPractice: Boolean(flags.vr),
  durationMinutes: flags.vr ? 25 : flags.assessment ? 20 : 14,
});

const modulesFor = (
  courseSlug: string,
  moduleOne: string,
  moduleTwo: string,
  theme: string,
): CourseModule[] => [
  {
    title: moduleOne,
    order: 1,
    lessons: [
      lesson(courseSlug, "orientation", "GoalVow pathway orientation", `${theme} starts with a clear goal, a support plan, and measurable progress.`),
      lesson(courseSlug, "practice-brief", "Practical skills brief", `Learners apply ${theme.toLowerCase()} through guided tasks, local examples, and facilitator checkpoints.`),
      lesson(courseSlug, "knowledge-check", "Knowledge check", `A short assessment confirms readiness before moving into applied practice.`, { assessment: true }),
    ],
  },
  {
    title: moduleTwo,
    order: 2,
    lessons: [
      lesson(courseSlug, "simulation-prep", "Simulation preparation", `Learners prepare for a realistic practice scenario linked to ${theme.toLowerCase()}.`),
      lesson(courseSlug, "vr-studio", "VR practice studio", `A WebXR-ready practice space supports future immersive training for ${theme.toLowerCase()}.`, { vr: true }),
      lesson(courseSlug, "completion-plan", "Completion and opportunity plan", `Learners convert completion evidence into certificates, rewards, and opportunity matches.`),
    ],
  },
];

const assessmentFor = (courseSlug: string, title: string): Assessment => ({
  slug: `${courseSlug}-knowledge-check`,
  lessonSlug: `${courseSlug}-knowledge-check`,
  title: `${title} assessment`,
  passMark: 70,
  questions: [
    {
      id: "q1",
      prompt: "What is the first learner action in the GoalVow pathway?",
      options: ["Skip to the certificate", "Set a clear goal", "Wait for an employer", "Open a forum"],
      answer: "Set a clear goal",
    },
    {
      id: "q2",
      prompt: "What does VowLMS track during learning?",
      options: ["Progress and attempts", "Only page views", "Forum votes", "Wiki edits"],
      answer: "Progress and attempts",
    },
    {
      id: "q3",
      prompt: "What unlocks after successful completion?",
      options: ["Rewards and opportunities", "Random modules", "A social profile", "A new password"],
      answer: "Rewards and opportunities",
    },
  ],
});

const vrFor = (courseSlug: string, title: string, scenario: string, skills: string[]): VRPractice => ({
  slug: `${courseSlug}-vr-studio`,
  lessonSlug: `${courseSlug}-vr-studio`,
  title: `${title} VR practice`,
  scenario,
  skillsPracticed: skills,
  scorePlaceholder: 86,
});

const course = (
  slug: string,
  academySlug: string,
  title: string,
  description: string,
  level: Course["level"],
  duration: string,
  price: number,
  theme: string,
  outcomes: string[],
  opportunityPathways: string[],
): Course => ({
  slug,
  title,
  academySlug,
  description,
  level,
  duration,
  price,
  status: "published",
  modules: modulesFor(slug, "Foundation readiness", "Applied practice pathway", theme),
  assessments: [assessmentFor(slug, title)],
  vrPractices: [
    vrFor(slug, title, `A guided ${theme.toLowerCase()} scenario with facilitator scoring and future headset support.`, [
      "Decision making",
      "Practical confidence",
      "Evidence capture",
    ]),
  ],
  outcomes,
  rewards: price === 0 ? 120 : 250,
  opportunityPathways,
});

